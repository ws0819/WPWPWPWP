package wine.beans;

public class SommelierBean {

	private int sommelier_number;
	private String sommelier_name;
	private String sommelier_tel;
	private String sommelier_email;
	
	public int getSommelier_number() {
		return sommelier_number;
	}
	public void setSommelier_number(int sommelier_number) {
		this.sommelier_number = sommelier_number;
	}
	public String getSommelier_name() {
		return sommelier_name;
	}
	public void setSommelier_name(String sommelier_name) {
		this.sommelier_name = sommelier_name;
	}
	public String getSommelier_tel() {
		return sommelier_tel;
	}
	public void setSommelier_tel(String sommelier_tel) {
		this.sommelier_tel = sommelier_tel;
	}
	public String getSommelier_email() {
		return sommelier_email;
	}
	public void setSommelier_email(String sommelier_email) {
		this.sommelier_email = sommelier_email;
	}
	
	
}
